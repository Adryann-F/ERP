    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTableFornecedores.getModel();
        FornecedorDAO fdao = new FornecedorDAO();
        modelo.setNumRows(0);
        for(Fornecedor f: fdao.lista()){
        modelo.addRow(new Object[]{
        f.getId(),
            f.getNome(),
            f.getCpf(),
            f.getCnpj(),
            f.getRgIr(),
            f.getObs()

        });
        
        }
    }
    //buscar a pessoa para a busca
        public void readJTableBusca(String nome){
        DefaultTableModel modelo = (DefaultTableModel) jTableFornecedores.getModel();
        FornecedorDAO fdao = new FornecedorDAO();
        modelo.setNumRows(0);
        for(Fornecedor f : fdao.busca(nome)){
        modelo.addRow(new Object[]{
        f.getId(),
            f.getNome(),
            f.getCpf(),
            f.getCnpj(),
            f.getRgIr(),
            f.getObs()

        });
        
        }
    }
