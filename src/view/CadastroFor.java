    private void jButtonAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAtualizarActionPerformed
        // atualizar os dados
         if(jTableFornecedores.getSelectedRow() != -1){
          Fornecedor f = new Fornecedor();
        FornecedorDAO  dao = new FornecedorDAO();
            // f.setId(Integer.parseInt(jTextFieldID.getText()));
            f.setNome(jTextFieldNome.getText());
            f.setCnpj(jFormattedTextFieldCNPJ.getText());
            f.setCpf(jFormattedTextFieldCPF.getText());
            f.setRgIr(jFormattedTextFieldRG.getText());
            f.setObs(jTextFieldOBS.getText());
            f.setId((int)jTableFornecedores.getValueAt(jTableFornecedores.getSelectedRow(), 0));
        dao.atualizar(f);
        readJTable();
        limpar(); 
         }else{
         JOptionPane.showMessageDialog(null, "Selecione um fornecedor para atualizar!");
         }
    }//GEN-LAST:event_jButtonAtualizarActionPerformed

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // excluir dados do banco 
         if(jTableFornecedores.getSelectedRow() != -1){
          Fornecedor f = new Fornecedor();
        FornecedorDAO  dao = new FornecedorDAO();
           f.setId(Integer.parseInt(jTextFieldID.getText()));
            
        dao.deletar(f);
        readJTable();
        limpar(); 
         
         
         }else{
         JOptionPane.showMessageDialog(null, "Selecione um fornecedor para ser excluido");
         }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

